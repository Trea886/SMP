"""
PAJ7620U2 手势传感器 — 官方原版驱动
SCL=8, SDA=9
"""

from machine import Pin, SoftI2C
import time

PAJ7620_ADDR = 0x73
PAJ_BANK_SELECT = 0xEF
PAJ_INT_FLAG1 = 0x43

GESTURE_MAP = {
    0x01: 'UP', 0x02: 'DOWN', 0x04: 'LEFT', 0x08: 'RIGHT',
    0x10: 'FORWARD', 0x20: 'BACKWARD',
    0x40: 'CLOCKWISE', 0x80: 'COUNTER_CLOCKWISE', 0x100: 'WAVE',
}

Init_Array = (
    (0xEF,0x00),(0x37,0x07),(0x38,0x17),(0x39,0x06),(0x41,0x00),(0x42,0x00),
    (0x46,0x2D),(0x47,0x0F),(0x48,0x3C),(0x49,0x00),(0x4A,0x1E),(0x4C,0x20),
    (0x51,0x10),(0x5E,0x10),(0x60,0x27),(0x80,0x42),(0x81,0x44),(0x82,0x04),
    (0x8B,0x01),(0x90,0x06),(0x95,0x0A),(0x96,0x0C),(0x97,0x05),(0x9A,0x14),
    (0x9C,0x3F),(0xA5,0x19),(0xCC,0x19),(0xCD,0x0B),(0xCE,0x13),(0xCF,0x64),
    (0xD0,0x21),(0xEF,0x01),(0x02,0x0F),(0x03,0x10),(0x04,0x02),(0x25,0x01),
    (0x27,0x39),(0x28,0x7F),(0x29,0x08),(0x3E,0xFF),(0x5E,0x3D),(0x65,0x60),
    (0x67,0x97),(0x69,0xA0),(0x6A,0x01),(0x6D,0x2C),(0x6E,0x01),(0x72,0x01),
    (0x73,0x35),(0x74,0x00),(0x77,0x01),
)

Gesture_Init = (
    (0xEF,0x00),(0x41,0x00),(0x42,0x00),(0xEF,0x00),(0x48,0x3C),(0x49,0x00),
    (0x51,0x10),(0x83,0x20),(0x9F,0xF9),(0xEF,0x01),(0x01,0x1E),(0x02,0x0F),
    (0x03,0x10),(0x04,0x02),(0x41,0x40),(0x43,0x30),(0x65,0x60),(0x66,0x00),
    (0x67,0x97),(0x68,0x01),(0x69,0xA0),(0x6A,0x01),(0x6B,0xB0),(0x6C,0x04),
    (0x6D,0x2C),(0x6E,0x01),(0x74,0x00),(0xEF,0x00),(0x41,0xFF),(0x42,0x01),
)

i2c = None

def init():
    global i2c
    i2c = SoftI2C(sda=Pin(9), scl=Pin(8))
    time.sleep_ms(500)

    # Power-up init
    for reg, val in Init_Array:
        try:
            i2c.writeto_mem(PAJ7620_ADDR, reg, bytes([val]))
        except:
            pass
        time.sleep_ms(5)

    # Gesture mode init
    i2c.writeto_mem(PAJ7620_ADDR, PAJ_BANK_SELECT, b'\x00')
    for reg, val in Gesture_Init:
        try:
            i2c.writeto_mem(PAJ7620_ADDR, reg, bytes([val]))
        except:
            pass
        time.sleep_ms(5)

    return True

def read_gesture():
    try:
        data = i2c.readfrom_mem(PAJ7620_ADDR, PAJ_INT_FLAG1, 2)
        val = (data[1] << 8) + data[0]
        if val != 0:
            # Clear interrupt
            i2c.writeto_mem(PAJ7620_ADDR, PAJ_INT_FLAG1, b'\x00\x00')
        return GESTURE_MAP.get(val, 'NONE')
    except:
        return 'NONE'
